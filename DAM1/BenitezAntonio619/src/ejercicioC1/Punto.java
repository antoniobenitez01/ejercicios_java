package ejercicioC1;

public class Punto 
{
	private int x;
	private int y;
	
// CONSTRUCTORES ===
	public Punto(int x, int y)
	{
		this.x = x;
		this.y = y;
	}
	
// GETTERS =========
	
	public int getX()
	{
		return this.x;
	}
	
	public int getY()
	{
		return this.y;
	}
	
// SETTERS ==========
	
	public void setX(int x)
	{
		this.x = x;
	}
	
	public void setY(int y)
	{
		this.y = y;
	}
}
