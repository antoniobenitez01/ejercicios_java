package variables;

public class EjemploVariables 
{

	public static void main(String[] args) 
	{
		int edad,anyoNacimiento;
		double peso, altura;
		String nombre, poblacion;
		
		edad = 22;
		anyoNacimiento = 2001;
		peso = 87.9;
		altura = 1.87;
		nombre = "Antonio";
		poblacion = "Benalmadena";
		
		System.out.println("La edad es: " + edad);
		System.out.println("El año de nacimiento es: " + anyoNacimiento);
		System.out.println("Su peso es: " + peso);
		System.out.println("Su altura es: " + altura);
		System.out.println("Su nombre es: " + nombre);
		System.out.println("Su población es: " + poblacion);
		
	}

}
